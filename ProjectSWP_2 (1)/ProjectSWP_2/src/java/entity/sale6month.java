package entity;

/**
 *
 * @author HP
 */
public class sale6month {
    private String name;
    private int total;

    public sale6month() {
    }

    public sale6month(String name, int total) {
        this.name = name;
        this.total = total;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public int getTotal() {
        return total;
    }

    public void setTotal(int total) {
        this.total = total;
    }

    @Override
    public String toString() {
        return "sale6month{" + "name=" + name + ", total=" + total + '}';
    }
    
}
